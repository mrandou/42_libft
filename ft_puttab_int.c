#include "libft.h"

void	ft_puttab_int(int **tab)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (tab[i])
	{
		while (tab[i][k])
			ft_putnbr(tab[i][k++]);
		i++;
		ft_putchar('\n');
	}
}
