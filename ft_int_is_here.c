#include "libft.h"

int	ft_int_is_here(int nb, int i)
{
	int tmp;

	tmp = 0;
	while (nb)
	{
		tmp = nb % 10;
		nb /= 10;
		if (i == tmp)
			return (1);
	}
	return (0);
}
