#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int k;
	int size;

	k = 0;
	size = ft_strlen(s1);
	while (s2[k])
		s1[size++] = s2[k++];
	s1[size] = '\0';
	return (s1);
}
