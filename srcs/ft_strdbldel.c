#include "libft.h"

void	ft_strdbldel(char **s1, char **s2)
{
	if (s1)
	{
		if (*s1)
		{
			free(*s1);
			*s1 = NULL;
		}
	}
	if (s2)
	{
		if (*s2)
		{
			free(*s2);
			*s2 = NULL;
		}
	}
}
